teamlist = []

# List of names to add to the teamlist
names = ['Alice', 'Bob', 'Charlie', 'Dave', 'Eve', 'Frank', 'Greta', 'Hannah', 'Igor', 'Jill', 'Kate']

# Use a for loop to iterate over the names and append them to the teamlist
for name in names:
    teamlist.append(name)

# Use a for loop to iterate over the teamlist and print each name
for name in teamlist:
    print(name)
